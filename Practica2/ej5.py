dia_num = int(input('Que dia de la semana es en numeros?'))
if dia_num == 1:
        print("Es Lunes")
elif dia_num == 2:
        print("Es Martes")
elif dia_num == 3:
        print("Es Miercoles")
elif dia_num == 4:
        print("Es Jueves")
elif dia_num == 5:
        print("Es Viernes")
elif dia_num == 6:
        print("Es Sabado")
elif dia_num == 7:
        print("Es Domingo")
else: 
        print("El numero es incorrecto")
